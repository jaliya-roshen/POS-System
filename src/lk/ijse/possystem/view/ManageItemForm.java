    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        String code = codeTextField1.getText();
        ItemDTO itemDTO;
        try {
            itemDTO = ItemController.searchItem(code);
            if (itemDTO != null){
                
                descTextField1.setText(itemDTO.getDescription());
                unitpriceTextField1.setText(String.valueOf(itemDTO.getUnitPrice()));
                qtyTextField.setText(Integer.toString(itemDTO.getQtyOnHand()));
                
                
            }else{
                JOptionPane.showMessageDialog(this, "No such Item");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ManageItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_SearchButtonActionPerformed

