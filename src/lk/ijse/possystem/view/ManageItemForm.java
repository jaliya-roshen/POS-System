    private void SaveButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButton1ActionPerformed

        try {

            boolean isAdded = ItemController.addItem(new ItemDTO(codeTextField1.getText(),
                    descTextField1.getText(),
                    Double.parseDouble(unitpriceTextField1.getText()),
                    Integer.parseInt(qtyTextField.getText()))
            );
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Item Added ok");
            } else {
                JOptionPane.showMessageDialog(this, "Item Added Fail");
            }

        } catch (Exception ex) {
            Logger.getLogger( ManageItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SaveButton1ActionPerformed

    private void UpdateButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButton1ActionPerformed
        
        try {
            boolean isUpdate = ItemController.updateItem(new ItemDTO(codeTextField1.getText(), descTextField1.getText(),
                    Double.parseDouble(unitpriceTextField1.getText()), Integer.parseInt(qtyTextField.getText())));
            
            if (isUpdate){
                JOptionPane.showMessageDialog(this, "Update Success");
            }else{
                
                JOptionPane.showMessageDialog(this, "Update Fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_UpdateButton1ActionPerformed

