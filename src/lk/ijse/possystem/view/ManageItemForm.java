    private void ViewButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewButton1ActionPerformed
         
        try {
            ArrayList<ItemDTO> items =  ItemController.getAllItems();
            DefaultTableModel dtm = (DefaultTableModel) ItemTable.getModel();
            dtm.setRowCount(0);
            for (ItemDTO item : items){
                
                Object [] ab ={item.getCode(), item.getDescription(), item.getUnitPrice(),item.getQtyOnHand()};
                dtm.addRow(ab);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ManageItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ViewButton1ActionPerformed

